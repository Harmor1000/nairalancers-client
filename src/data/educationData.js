// Utility functions for search and filtering
export const searchUniversities = (query) => {
  if (!query || query.length < 2) return [];
  const lowerQuery = query.toLowerCase();
  return nigerianUniversities.filter(university => 
    university.toLowerCase().includes(lowerQuery)
  ).slice(0, 10); // Limit to 10 results
};

export const searchFieldsOfStudy = (query) => {
  if (!query || query.length < 2) return [];
  const lowerQuery = query.toLowerCase();
  return fieldsOfStudy.filter(field => 
    field.toLowerCase().includes(lowerQuery)
  ).slice(0, 10); // Limit to 10 results
};

export const getDegreeTypes = () => {
  return degreeTypes;
};
